// Faça um programa que compare um número com 10 e imprime na tela "o número é maior, o número é menor ou número é igual"

const myNumber = 110

if(myNumber < 10){
    console.log(`o número ${myNumber} é menor do que o numero 10`)
} else if (myNumber == 10) {
    console.log(`o número é igual ao numero 10`)
} else {
    console.log(`o número ${myNumber} é maior do que o numero 10`)
}