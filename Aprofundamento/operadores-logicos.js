/* OPERADORES LOGICOS 

&& → E     // FILHO OBEDIENTE
    true && true = true 
    true && false = false 
    false && false = false 

|| → Ou     // FILHO ESPERTINHO
    true || true = true 
    true || false = true 
    false || false = false 

! → Negação     // FILHO DO CONTRA, REBELDE
    !true = false 
    !false = true 

    
    
    O que precisa para alguem fazer um transferencia bancaria?
    
    - Saldo em conta
    - conta nao bloqueiada
    - conta destino correta
    
*/

// EX:

    console.log(true && false) // E
    
    console.log(true || false || false || false) // OU
    
    console.log(!true) // NEGACAO

   
   
   